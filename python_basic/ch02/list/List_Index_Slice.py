# list index
a = [1, 2, 3]
print(a)
print(a[0])
print(a[0] + a[2])
print(a[-1])
print()

b = [1, 2, 3, ['a', 'b', 'c']]
print(b[0])
print(b[-1])
print(b[3])
print()
c = [1, 2, ['a', 'b',['life', 'is']]]
print(c[2][2][0])
print()

# list slicing
d = [1, 2, 3, 4, 5]
print(d[0:2])
print(d[:2])
print(d[2:])
print()

